public class CaracteresEspeciales {
    public static void main (String args[]) {
        var nombres = " Eli y David ";
        
        System.out.println("Nueva linea : \n" + nombres);
        System.out.println("Tabulador : \t" + nombres);
        System.out.println("Retroceso : \b" + nombres);
        System.out.println("Comilla simple \'" + nombres + "\'");
        System.out.println("Comilla doble : \"" + nombres + "\"" );
        
        
    }
}
